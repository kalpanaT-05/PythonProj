# less than operator
print(2 < 2)

# less than or equal to operator
print(2 <= 2)

# greater than operator
print(2 > 2)

# greater than or equal to operator
print(2 >= 2)

# equal to operator
print(2 == 2)

# not equal to operator
print(2 != 2)

# """modulus(mod) operator used for returning the resultant or remainder
# by dividing the numerator i.e., 2 by denominator i.e., 4 and
# resultant/remainder should be 2"""
print(2 % 4)